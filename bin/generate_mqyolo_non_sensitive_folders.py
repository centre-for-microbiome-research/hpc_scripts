#!/usr/bin/env python3
"""Generate mqyolo-non-sensitive-folders.json from the CMR work-folders sheet.

Downloads the shared Google Sheet that lists each /work/ project folder and
whether it may contain sensitive (IP-related or patient) data, then writes the
subset that is explicitly marked "not sensitive" to a JSON config file.

mqyolo reads that JSON each time it starts and auto-mounts the listed folders
read-only inside the sandbox container (see bin/mqyolo). Folders that are marked
sensitive, or whose sensitivity is left blank/unknown, are deliberately NOT
included, so the default is to keep them hidden.

Re-run after the sheet changes with:

    pixi run update-non-sensitive-folders
"""

import csv
import datetime
import io
import json
import os
import sys
import urllib.request

# The "work folders" sheet. gid pins the specific tab/worksheet.
SHEET_ID = "1D4nj87RrU9t4pnsPQCmmd-ikIFdTwhpVmacAHH4j04Y"
SHEET_GID = "1575462225"
SHEET_EDIT_URL = (
    "https://docs.google.com/spreadsheets/d/{id}/edit?gid={gid}#gid={gid}".format(
        id=SHEET_ID, gid=SHEET_GID))
SHEET_CSV_URL = (
    "https://docs.google.com/spreadsheets/d/{id}/export?format=csv&gid={gid}".format(
        id=SHEET_ID, gid=SHEET_GID))

# Column indices (0-based) used from the sheet:
#   column 1 (index 0): the folder path, e.g. /work/microbiome/abisko
#   column 4 (index 3): sensitivity, one of "not sensitive" / "sensitive" / ""
PATH_COLUMN = 0
SENSITIVITY_COLUMN = 3
NOT_SENSITIVE_VALUE = "not sensitive"

# Written next to the repo root so mqyolo can find it via a relative path
# (../mqyolo-non-sensitive-folders.json from bin/).
OUTPUT_FILENAME = "mqyolo-non-sensitive-folders.json"


def download_csv(url):
    req = urllib.request.Request(url, headers={"User-Agent": "mqyolo-folder-sync"})
    with urllib.request.urlopen(req, timeout=60) as resp:
        return resp.read().decode("utf-8")


def extract_non_sensitive_folders(csv_text):
    folders = []
    seen = set()
    reader = csv.reader(io.StringIO(csv_text))
    for i, row in enumerate(reader):
        if len(row) <= SENSITIVITY_COLUMN:
            continue
        path = row[PATH_COLUMN].strip()
        sensitivity = row[SENSITIVITY_COLUMN].strip().lower()
        # Only keep absolute paths explicitly flagged as not sensitive. This
        # skips the header row, blank rows, and anything sensitive/unmarked.
        if not path.startswith("/"):
            continue
        if sensitivity != NOT_SENSITIVE_VALUE:
            continue
        if path in seen:
            continue
        seen.add(path)
        folders.append(path)
    return sorted(folders)


def main():
    script_dir = os.path.dirname(os.path.realpath(__file__))
    output_path = os.path.join(script_dir, "..", OUTPUT_FILENAME)
    output_path = os.path.normpath(output_path)

    sys.stderr.write("Downloading sheet from {}\n".format(SHEET_CSV_URL))
    csv_text = download_csv(SHEET_CSV_URL)
    folders = extract_non_sensitive_folders(csv_text)
    if not folders:
        sys.stderr.write(
            "ERROR: no 'not sensitive' folders parsed from the sheet; refusing "
            "to overwrite {} with an empty list.\n".format(output_path))
        return 1

    config = {
        "_comment": (
            "Auto-generated by bin/generate_mqyolo_non_sensitive_folders.py. "
            "Folders listed here are marked 'not sensitive' in the CMR work-folders "
            "sheet and are auto-mounted read-only inside the mqyolo sandbox. Do not "
            "edit by hand; re-run `pixi run update-non-sensitive-folders`."),
        "source_url": SHEET_EDIT_URL,
        "generated_utc": datetime.datetime.now(datetime.timezone.utc)
                                  .strftime("%Y-%m-%dT%H:%M:%SZ"),
        "non_sensitive_folders": folders,
    }

    with open(output_path, "w") as f:
        json.dump(config, f, indent=2)
        f.write("\n")

    sys.stderr.write(
        "Wrote {} non-sensitive folder(s) to {}\n".format(len(folders), output_path))
    return 0


if __name__ == "__main__":
    sys.exit(main())
